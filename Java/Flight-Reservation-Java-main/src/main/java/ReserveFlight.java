    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        back.readFiles();
        int zxz = back.reservations.size()+1;
        reserveId = String.valueOf(zxz);
        if(flightCode == null){
            javax.swing.JOptionPane.showMessageDialog(null, "Invalid!");
        } else {
          back.makeReservation(reserveId, customerId, this.totalPass, this.price, flightCode);
          javax.swing.JOptionPane.showMessageDialog(null, "Reserved!");
          this.setVisible(false);
          new MainMenu().setVisible(true);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String cLabel = codeLabel.getText().trim();
      
        if(back.getFlightData(cLabel) != null){
            flyFrom.setText(back.getFlightData(cLabel).flyFrom);
            flyTo.setText(back.getFlightData(cLabel).flyTo);
            this.flightCode = cLabel;
            jLabel12.setText(Double.toString(back.getFlightData(cLabel).price));
            jLabel11.setText("Success");
            
        } else{
            jLabel11.setText("Invalid Code");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

