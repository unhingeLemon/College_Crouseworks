    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        // CALCULATE
       int index = child.getSelectedIndex();
       
       int c = Integer.parseInt(child.getItemAt(index));
       index = adult.getSelectedIndex();
       int a = Integer.parseInt(child.getItemAt(index));
       index = infants.getSelectedIndex();
       int i = Integer.parseInt(child.getItemAt(index));
     
       int totalPassenger = c + a + i;      
       this.totalPass = String.valueOf(totalPassenger);
       String cLabel = codeLabel.getText().trim();
        
        if(back.getFlightData(cLabel) != null){
           double p = back.getFlightData(cLabel).price;
           double result = p * totalPassenger;
           String ll = Double.toString(result);
           this.price = ll;
            jLabel9.setText(ll);
        } else{
           
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

